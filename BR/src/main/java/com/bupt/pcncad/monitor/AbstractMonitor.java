public abstract class AbstractMonitor {

    protected boolean on = false;

    public abstract void on(boolean first) throws Exception;

    public abstract void off() throws Exception;

    public void setOn(boolean on) {
        this.on = on;
    }

    public boolean isOn() {
        return this.on;
    }
}

